package Polymorphism;

public class Operation {
	int sum(int a, int b) {
		return a + b;
	}

	int sum(int a, int b, int c) {
		return a + b + c;
	}

	int sum(int a, int b, int c, int d) {
		return a + b + c + d;
	}

	int sum(int a, int b, int c, int d, int e) {
		return a + b + c + d + e;
	}

	int sum(int a, int b, int c, int d, int e, int f) {
		return a + b + c + d + e + f;
	}

}
